package com.claudecode.tools;

import com.claudecode.core.engine.ToolExecutionContext;
import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.node.ObjectNode;

import java.util.function.Function;

/**
 * BriefTool — generates a brief summary for a given topic.
 * Task 49.1
 *
 * Uses LLM-based summarization when a summarizer is configured,
 * otherwise falls back to keyword-based analysis.
 */
public class BriefTool extends Tool<JsonNode, String> {

    /**
     * Functional interface for LLM-based brief summarization.
     */
    @FunctionalInterface
    public interface BriefSummarizer {
        /**
         * Generate a brief summary for the given topic.
         *
         * @param topic     the topic to summarize
         * @param maxLength maximum length in words
         * @return the generated brief summary
         */
        String summarize(String topic, int maxLength);
    }

    private BriefSummarizer summarizer;

    public BriefTool() {
        this(null);
    }

    public BriefTool(BriefSummarizer summarizer) {
        this.summarizer = summarizer;
    }

    /**
     * Set the summarizer for LLM-based brief generation.
     */
    public void setSummarizer(BriefSummarizer summarizer) {
        this.summarizer = summarizer;
    }

    @Override
    public String name() { return "Brief"; }

    @Override
    public String description() {
        return "Generate a brief summary for a topic or question. Use this to get concise overviews.";
    }

    @Override
    public JsonNode inputSchema() {
        ObjectNode schema = createObjectSchema();
        ObjectNode props = mapper().createObjectNode();
        props.set("topic", mapper().createObjectNode()
            .put("type", "string")
            .put("description", "The topic or question to summarize"));
        props.set("max_length", mapper().createObjectNode()
            .put("type", "integer")
            .put("description", "Maximum length of the summary in words (default: 100)"));
        schema.set("properties", props);
        schema.set("required", mapper().createArrayNode().add("topic"));
        return schema;
    }

    @Override
    public String call(JsonNode input, ToolExecutionContext context) {
        String topic = input.has("topic") ? input.get("topic").asText() : "";
        int maxLength = input.has("max_length") ? input.get("max_length").asInt(100) : 100;

        if (topic.isBlank()) {
            return "Error: topic is required and cannot be empty.";
        }

        if (summarizer != null) {
            return generateLlmBrief(topic, maxLength);
        }

        return generateKeywordBrief(topic, maxLength);
    }

    private String generateLlmBrief(String topic, int maxLength) {
        try {
            String summary = summarizer.summarize(topic, maxLength);
            return "Brief Summary\n" +
                   "============\n\n" +
                   summary + "\n\n" +
                   "(Generated by LLM)";
        } catch (Exception e) {
            return "Brief generation failed: " + e.getMessage() + "\n" +
                   "Falling back to keyword analysis...\n\n" +
                   generateKeywordBrief(topic, maxLength);
        }
    }

    private String generateKeywordBrief(String topic, int maxLength) {
        String[] words = topic.trim().split("\\s+");
        int wordCount = words.length;

        StringBuilder summary = new StringBuilder();
        summary.append("Topic: ").append(topic).append("\n\n");

        if (wordCount <= 3) {
            summary.append("This is a short query about \"").append(topic).append("\".\n");
            summary.append("To get a meaningful brief, consider providing more context.\n\n");
            summary.append("Key terms: ");
            summary.append(String.join(", ", words));
        } else if (wordCount <= 10) {
            summary.append("Key aspects of this topic:\n");
            for (String word : words) {
                if (word.length() > 4) {
                    summary.append("  • ").append(word).append("\n");
                }
            }
            summary.append("\nFor detailed analysis, try /compact to summarize related context.\n");
        } else {
            summary.append("This appears to be a detailed query.\n");
            summary.append("Consider breaking it into smaller sub-topics:\n");
            for (int i = 0; i < Math.min(5, wordCount); i++) {
                summary.append("  ").append(i + 1).append(". ").append(words[i]).append("\n");
            }
            summary.append("\nFor full LLM-generated brief, use /compact to preserve context.\n");
        }

        return summary.toString();
    }

    @Override
    public boolean isReadOnly() { return true; }

    @Override
    public boolean isConcurrencySafe() { return true; }
}
