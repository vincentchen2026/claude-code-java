package com.claudecode.tools;

import com.claudecode.core.engine.ToolExecutionContext;
import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.node.ObjectNode;

import java.nio.file.Files;
import java.nio.file.Path;

/**
 * ReviewArtifactTool — reviews an artifact file.
 * Task 55.1
 */
public class ReviewArtifactTool extends Tool<JsonNode, String> {

    @Override
    public String name() { return "ReviewArtifact"; }

    @Override
    public String description() {
        return "Review an artifact file. Reads the file and provides a structured review summary.";
    }

    @Override
    public JsonNode inputSchema() {
        ObjectNode schema = createObjectSchema();
        ObjectNode props = (ObjectNode) schema.get("properties");

        ObjectNode pathSchema = mapper().createObjectNode();
        pathSchema.put("type", "string");
        pathSchema.put("description", "Absolute path to the artifact file to review");
        props.set("artifact_path", pathSchema);

        schema.set("required", mapper().createArrayNode().add("artifact_path"));
        return schema;
    }

    @Override
    public String call(JsonNode input, ToolExecutionContext context) {
        String artifactPath = input.has("artifact_path") ? input.get("artifact_path").asText() : null;
        if (artifactPath == null || artifactPath.isBlank()) {
            return "Error: artifact_path is required.";
        }

        Path path = Path.of(artifactPath);
        if (!Files.exists(path)) {
            return String.format("Error: file not found: %s", artifactPath);
        }
        if (!Files.isRegularFile(path)) {
            return String.format("Error: not a regular file: %s", artifactPath);
        }

        try {
            long size = Files.size(path);
            String content = Files.readString(path);
            int lineCount = content.split("\n", -1).length;

            return String.format(
                "Artifact Review: %s\n" +
                "  Size: %d bytes\n" +
                "  Lines: %d\n" +
                "  [Full review would be generated by LLM analysis in complete implementation]",
                artifactPath, size, lineCount);
        } catch (Exception e) {
            return String.format("Error reading artifact: %s", e.getMessage());
        }
    }

    @Override
    public boolean isReadOnly() { return true; }

    @Override
    public boolean isConcurrencySafe() { return true; }
}
